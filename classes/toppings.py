class Toppings():
    def __init__(self):
        self.name = None
        self.cost = 0

    
    # Getter and Setter for name
    def get_name(self):
        return self.name


    def set_name(self, value):
        self.name = value


    # Getter and Setter for cost
    def get_cost(self):
        return self.cost


    def set_cost(self, value):
        self.cost = value